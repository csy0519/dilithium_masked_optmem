#define L 4
#define ETA 2
#define TAU 39
#define BETA 78
#define GAMMA1 (1 << 17)
#define GAMMA2 ((Q-1)/88)
#define OMEGA 80
#define PQCLEAN_DILITHIUM2_MASK_OPTMEM_CRYPTO_ALGNAME "Dilithium2"

#define QP 257

#define NSHARES 2

/* Maximum value encountered in dilithium versions */
#define TAU_MAX 60 

#define POLY_BYTES N * 4
