#define PQCLEAN_DILITHIUM2_MASK_OPTMEM_VER_BUF_BYTES (SEEDBYTES + CRHBYTES + 3*POLYVECK_BYTES + POLYVECL_BYTES + 2*POLY_BYTES + MAT_BYTES)

#endif
